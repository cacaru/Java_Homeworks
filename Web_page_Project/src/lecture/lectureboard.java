package lecture;

public class lectureboard {
	private String lid;
	private int no;
	private int division;
	private String title;
	private String date;
	private String content;
	private String file;
	private int availavle;
	private String prePerson;
	public String getLid() {
		return lid;
	}
	public void setLid(String lid) {
		this.lid = lid;
	}
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public int getDivision() {
		return division;
	}
	public void setDivision(int division) {
		this.division = division;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getFile() {
		return file;
	}
	public void setFile(String file) {
		this.file = file;
	}
	public int getAvailavle() {
		return availavle;
	}
	public void setAvailavle(int availavle) {
		this.availavle = availavle;
	}
	public String getPrePerson() {
		return prePerson;
	}
	public void setPrePerson(String prePerson) {
		this.prePerson = prePerson;
	}
	
}
